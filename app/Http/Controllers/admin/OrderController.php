    public function shipped(Request $request, $id){
        if($request->user()->name){
            $order = Order::where('id', '=', $id)->first();
            if($order->restaurant->owner->id == Auth::user()->id){
                $order->shipped = 1;
                $order->save();
                return redirect()->back();
            }
            else{
                $order->shipped = 0;
                return redirect()->back();
            }
        }
    }
